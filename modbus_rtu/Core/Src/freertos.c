/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* init code for LWIP */
  MX_LWIP_Init();
  /* USER CODE BEGIN StartDefaultTask */
    osDelay(1000);
    mbport_serial_t* port_serial = mbport_serial_create(3);
    modbus_t *mb = modbus_create(MODBUS_MODE_RTU, (void*)&(port_serial->parent));
    modbus_set_slave(mb, 0x01);
    modbus_set_recvtmo(mb, 500); // 500ms timeout
    // connect
    if (modbus_connect(mb) != MB_ENOERR) {
        Error_Handler();
        printf_rtos("modbus_connect error\n");
    }
    printf_rtos("modbus_connect success\n");

  /* Infinite loop */
  for(;;)
  {
      mb_uint16_t reg_value;
      // test: read address 0x00 ,1 word
      mb_err_t err_t = modbus_read_holding_regs(mb, 0x00, 0x01, &reg_value);
      if (err_t == MB_ENOERR) {
          printf_rtos("Read register value: %d\n", reg_value);
      } else {
          printf_rtos("Error: %s\n", modbus_strerr(err_t));
      }
    osDelay(pdMS_TO_TICKS(4000));
  }

  /* USER CODE END StartDefaultTask */
}

